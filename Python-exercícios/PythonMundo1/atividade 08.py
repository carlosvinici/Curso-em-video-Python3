M = float(input('Número à ser convertido: '))
DAM = M / 10
HM = M / 100
KM = M / 1000
DM = M * 10
CM = M * 100
MM = M * 1000
print('O número {:.2F}m convertido fica:\n Em Decametro: {:.2f}dam\n'
      ' Em Hectametro: {:.2f}hm\n Em Kilometro: {:.2f}km\n'
      ' Em Decemetro: {:.2f}dm\n Em Centimetros: {:.2f}cm\n'
      ' Em melimetros: {:.2f}mm\n OBRIGADOO!!'.format(M, DAM, HM, KM, DM, CM, MM))
